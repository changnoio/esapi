/** Generate the full ESAPI C# plug-in source for a site configuration. */
export function generateScript(cfg: SiteConfig, stamp = utcStamp()): string {
  return buildHeader(cfg, stamp) + buildConfig(cfg) + STATIC_BODY;
}

/** Suggested file name for the generated script. */
export function scriptFileName(cfg: SiteConfig): string {
  const key = (cfg.siteKey || "Site").replace(/[^A-Za-z0-9_-]/g, "");
  return `AutoPlan_${key}.cs`;
}
