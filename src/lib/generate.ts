function buildTargetLevels(cfg: SiteConfig): string {
  if (cfg.targetLevels.length === 0) {
    return "        {\n            // (none - REQUIRES MAPPING)\n        };";
  }
  const lines = cfg.targetLevels
    .map((t) => `            { ${cs(t.structureId)}, new DoseValue(${num(t.dose)}, ${cs(t.unit)}) },`)
    .join("\n");
  return `        {\n${lines}\n        };`;
}

function buildStructureMatches(cfg: SiteConfig): string {
  if (cfg.structureMatches.length === 0) {
    return "        {\n            // (none - REQUIRES MAPPING)\n        };";
  }
  const lines = cfg.structureMatches
    .map((m) => `            { ${cs(m.patientId)}, ${cs(m.modelId)} },`)
    .join("\n");
  return `        {\n${lines}\n        };`;
}

function buildSuggested(cfg: SiteConfig): string {
  const n = cfg.suggestedMatches.length;
  const head =
    `        // ---- Suggested matches for this site (planner to complete if mapping required) ----\n` +
    `        // ${n} suggestion(s); uncomment & set the model structure IDs:`;
  if (n === 0) {
    return head + "\n            // (no suggestions)";
  }
  const lines = cfg.suggestedMatches
    .map((s) => `            // matches.Add(${cs(s)}, "<model structure id>");`)
    .join("\n");
  return head + "\n" + lines;
}

function buildArcs(cfg: SiteConfig): string {
  const lines = cfg.arcs
    .map(
      (a) =>
        `            new ArcSpec { Id = ${cs(a.id)}, CollimatorAngle = ${num(a.collimatorAngle)}, ` +
        `GantryStart = ${num(a.gantryStart)}, GantryStop = ${num(a.gantryStop)}, ` +
        `Direction = ${csDirection(a.direction)}, DoseRate = ${num(a.doseRate)}, ` +
        `X1 = ${num(a.x1)}, Y1 = ${num(a.y1)}, X2 = ${num(a.x2)}, Y2 = ${num(a.y2)}, ` +
        `AvoidSectors = ${csAvoidSectors(a.avoidSectors)} },`
    )
    .join("\n");
  return lines;
}

function buildSetupFields(cfg: SiteConfig): string {
  return cfg.setupFields
    .map(
      (s) =>
        `            new SetupSpec { Id = ${cs(s.id)}, Gantry = ${num(s.gantry)}, ` +
        `X1 = ${num(s.x1)}, Y1 = ${num(s.y1)}, X2 = ${num(s.x2)}, Y2 = ${num(s.y2)} },`
    )
    .join("\n");
}

