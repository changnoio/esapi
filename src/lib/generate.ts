import { Arc, SiteConfig } from "./types";
import { STATIC_BODY } from "./staticBody";

/** Escape a JS string for use as a C# double-quoted literal. */
function cs(s: string): string {
  return '"' + s.replace(/\\/g, "\\\\").replace(/"/g, '\\"') + '"';
}

/** Format a number the way it should appear in C# source (no thousands sep). */
function num(n: number): string {
  if (!Number.isFinite(n)) return "0";
  return String(n);
}

function bool(b: boolean): string {
  return b ? "true" : "false";
}

function csDirection(d: Arc["direction"]): string {
  return d === "CW" ? "GantryDirection.Clockwise" : "GantryDirection.CounterClockwise";
}

function csAvoidSectors(sectors: Arc["avoidSectors"]): string {
  if (!sectors || sectors.length === 0) return "new double[0][]";
  const inner = sectors
    .map(([a, b]) => `new double[] { ${num(a)}, ${num(b)} }`)
    .join(", ");
  return `new double[][] { ${inner} }`;
}

/** Current UTC timestamp formatted like the templates: "YYYY-MM-DD HH:mm:ss UTC". */
function utcStamp(d = new Date()): string {
  const p = (n: number) => String(n).padStart(2, "0");
  return (
    `${d.getUTCFullYear()}-${p(d.getUTCMonth() + 1)}-${p(d.getUTCDate())} ` +
    `${p(d.getUTCHours())}:${p(d.getUTCMinutes())}:${p(d.getUTCSeconds())} UTC`
  );
}

